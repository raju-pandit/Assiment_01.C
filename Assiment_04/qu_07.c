
//7. Write a program to print the first 10 even natural numbers in reverse order

#include<stdio.h>
int main()
{
     int i;
     printf("The first 10 even natural number is: \n");

     for ( i = 10; i >=0; i--)
     {
          printf("%d\n",2*i);
     }
     printf("the even natural numbers in reverse order%d",i);

}